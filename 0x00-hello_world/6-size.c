#include <stdio.h>

/**
 * main - Entry point
 *
 * Return: Always 0 (Sucess)
 */

int main(void)
{
	char c;
	int i;
	long int li;
	long long ll;
	float f;

	printf("Size of a char: %c", c);
	printf("Size of an int: %d", i);
	printf("Size of a long int: %ld", li);
	printf("Size of a long long int: %lf", ll);
	printf("Size of a float: %f", f);

	return (0);
}

